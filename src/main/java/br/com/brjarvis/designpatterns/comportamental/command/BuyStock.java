package br.com.brjarvis.designpatterns.comportamental.command;

/**
 * Classe concreta de compra
 */
public class BuyStock implements Order {
    private Stock abcStock;

    public BuyStock(Stock abcStock) {
        this.abcStock = abcStock;
    }

    @Override
    public void execute() {
        abcStock.buy();
    }
}
